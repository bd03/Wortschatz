    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:    
        
        try{
            //write what has been written
            FileInputStream fstream = new FileInputStream("Words.dat");
            DataInputStream in = new DataInputStream(fstream);
            BufferedReader br = new BufferedReader(new InputStreamReader(in));
        
            
            StringBuilder buf = new StringBuilder(); 
            String strLine;
            //Read File Line By Line
            while ((strLine = br.readLine()) != null)   {
                buf.append( strLine ).append( lineSeparator );                
            }
            
            in.close();
            
            BufferedWriter  ou = new BufferedWriter(new FileWriter("Words.dat"));
            ou.write(buf.toString());
            //write new words
            for(int i=0;i<20;i++){
                if(!jT[i*4+1].getText().equals("")){
                    ou.write(jT[i*4].getText());
                    ou.newLine();
                    ou.write(jT[i*4+1].getText());
                    ou.newLine();
                    ou.write(jT[i*4+2].getText());
                    ou.newLine();
                    ou.write(jT[i*4+3].getText());
                    ou.newLine();
                    ou.write(jC[i].getSelectedIndex()+"");
                    ou.newLine();
                }
            }
            
            ou.close();
        }catch(Exception e){
            System.err.println("Error: " + e.getMessage());
        }
        
        this.setVisible(false);
        wA.listWordsAdmin();
        wA.updateMainFrame();
    }//GEN-LAST:event_jButton1ActionPerformed

