    private void languageAndWordlistCheck() {    
        String language="", wordbook="";      
        try{
            // Open the file that is the first 
            // command line parameter
            FileInputStream fstream = new FileInputStream("Options.dat");
            // Get the object of DataInputStream
            DataInputStream in = new DataInputStream(fstream);
            BufferedReader br = new BufferedReader(new InputStreamReader(in));
            String strLine;
            //Read File Line By Line
            int count =0;
            
            while ((strLine = br.readLine()) != null)   {
                // Print the content on the console
                if(count == 0){
                    wordbook = strLine;
                }else if(count == 1){
                    language = strLine;
                }
                count++;
            }
            //Close the input stream
            in.close();
        }catch (Exception e){//Catch exception if any
            System.err.println("Error: " + e.getMessage());
        }
        
        //prepare the language selected:
        if(Integer.parseInt(language) == 0){
            jButton1.setText("Hinzufügen");
            jLabel1.setText("der Artikel");
            jLabel2.setText("das Wordt");
            jLabel3.setText("der Plural");
            jLabel4.setText("die Bedeutung");
            setjComboBox(new String[] { "Neue Wörter", "Nicht so sicher", "Gut bekannt" });
            this.setTitle("Neue Wörter hinzufügen");
        }else if(Integer.parseInt(language) == 1){
            jButton1.setText("Ekle");
            jLabel1.setText("Artikel");
            jLabel2.setText("Kelime");
            jLabel3.setText("Çoğulu");
            jLabel4.setText("Anlamı");
            setjComboBox(new String[] { "Yeni Kelimeler", "Emin olunmayanlar", "İyi bilinenler" });
            this.setTitle("Yeni kelime ekle");
        }else{
            jButton1.setText("Add");
            jLabel1.setText("Article");
            jLabel2.setText("Word");
            jLabel3.setText("Plural Form");
            jLabel4.setText("Meaning");
            setjComboBox(new String[] { "New Words", "Not so safe", "Well known" });
            this.setTitle("Add new words");
        }
    }
    
